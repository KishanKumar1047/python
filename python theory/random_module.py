
# random module
import random

l = [10,20,30,40]
print(random.choice(l))
# return element from list random

print(random.random())
#return float value between 0 and 1

l = [10,20,30,40]
print(random.shuffle(l))
#shuffle element of list

print(random.uniform(3,9))
# randomfloat  value between 3 and 9

print(random.randint(3,9))
# random integer  value between 3 and 9 both included

print(random.randrange(3,9))
# random integer  value between 3  included and excluded


